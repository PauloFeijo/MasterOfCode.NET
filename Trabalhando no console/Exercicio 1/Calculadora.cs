namespace Exercicio_1
{
    public class Calculadora    
    {
        public double valorA;
        public double valorB;

        public double Somar()
        {
            return valorA + valorB;
        }

        public double Subtrair()
        {
            return valorA - valorB;
        }

        public double Dividir()
        {
            return valorB / valorA;
        }        

        public double Multiplicar()
        {
            return valorA * valorB;
        }
    }
}
